function getScenarioCatalog(options = {}) {
  const mode = options.mode || null;
  return SCENARIOS.filter((scenario) => {
    if (!mode) return true;
    return scenario.mode_support.includes(mode);
  }).map((scenario) => ({ ...scenario }));
}

